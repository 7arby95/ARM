#include "SysCtl.h"
#include "NVIC.h"
#include "Port.h"
#include "DIO.h"
#include "ADC.h"


/********************************************************************************
 *								 Type  Declaration								*
 ********************************************************************************/


/********************************************************************************
 * 								Preprocessor Macros								*
 ********************************************************************************/


/********************************************************************************
 * 							  Global Static Variables							*
 ********************************************************************************/


/********************************************************************************
 * 							  	   Main Function								*
 ********************************************************************************/

int main(void)
{
	uint16_t Result;

	SysCtl_Init();
	NVIC_Init();
	Port_Init();
	
	ADC_Init();

	while(1)
	{
		ADC_StartConversion();
		ADC_Read(&Result);
		PORT_A->GPIOAFSEL = Result;
	}
}
